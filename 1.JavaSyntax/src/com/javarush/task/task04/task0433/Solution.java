package com.javarush.task.task04.task0433;


/* 
Гадание на долларовый счет
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        int i = 0;
        while (i < 10){
            int k = 0;
            while (k < 10){
                System.out.print("S");
                k++;
            }
            System.out.println();
            i++;
        }

    }
}
